package com.lqj.entity;

public class DiagnoseSum {
    private int diagnoseSum;
    private int cureSum;
    private int deadSum;
    private String date;

    public int getDiagnoseSum() {
        return diagnoseSum;
    }

    public void setDiagnoseSum(int diagnoseSum) {
        this.diagnoseSum = diagnoseSum;
    }

    public int getCureSum() {
        return cureSum;
    }

    public void setCureSum(int cureSum) {
        this.cureSum = cureSum;
    }

    public int getDeadSum() {
        return deadSum;
    }

    public void setDeadSum(int deadSum) {
        this.deadSum = deadSum;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
